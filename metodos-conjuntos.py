dic = {
    'Nome': 'Susana',
    'Idade': 26
}

print(dic) # 'Nome': 'Susana', 'Idade': 26
print(dic.keys()) # 'Nome', 'Idade'
print(dic.values()) # 'Susana', 26
print(dic.items()) # ('Nome', 'Susana'), ('Idade', 26)


